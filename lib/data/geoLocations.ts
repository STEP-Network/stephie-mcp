// Helper function to search geo locations
export function searchGeoLocations(
	search?: string,
	type?: GeoLocation["type"],
	limit = 20,
): GeoLocation[] {
	let results = GEO_LOCATIONS;

	// Filter by type if specified
	if (type) {
		results = results.filter((loc) => loc.type === type);
	}

	// Search in name and canonicalName if search term provided
	if (search) {
		const searchLower = search.toLowerCase();
		results = results.filter(
			(loc) =>
				loc.name.toLowerCase().includes(searchLower) ||
				loc.canonicalName.toLowerCase().includes(searchLower),
		);
	}

	// Return limited results
	return results.slice(0, limit);
}

// Helper to get location by criteriaId
export function getLocationByCriteriaId(
	criteriaId: string,
): GeoLocation | undefined {
	const idNum = Number(criteriaId);
	return GEO_LOCATIONS.find((loc) => loc.criteriaId === idNum);
}

// Helper to get locations by parent
export function getLocationsByParent(parentId: string): GeoLocation[] {
	const parentIdNum = Number(parentId);
	return GEO_LOCATIONS.filter((loc) => loc.parentId === parentIdNum);
}
